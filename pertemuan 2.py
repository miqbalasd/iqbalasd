#tipe data di python
#tipe data digunakan untuk cek tipe data
#10 merupakan tipe data int
print(type(10))

#float merupakan tipe data angka desimal
print(99.9)
print(type(99.9))

#string merupakan tipe data menggunakan petik dua atau petik satu
print("93")
print(type("93"))

#boolean merupakan tipe data yang haya berisi data true atau false
print(type(True))
print(type(False))

#variabel
#konversi int ke str
nilai = 98
print(nilai)
print(type(nilai))
print(type(str(nilai)))

#konversi int ke float
print(type(float(nilai)))

print("tipe data int : ",nilai)
print("tipe data str : ",str(nilai))
print("tipe data int : ",float(nilai))

#konversi Boolean ke int
student = True
print(type(int(student)))
print("tipe data int : ",int(student))

